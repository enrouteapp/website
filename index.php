<br /><br />

<p>EnRoute is a suite of applications for Android mobile phones, vehicles, and web clients that is designed to facilitate real time communication between
drivers and passengers. By improving communication between these two parties, we believe that the carpooling experience can be made safer 
and more enjoyable.</p>

<p>While fully operational, EnRoute was developed as a proof of concept application. There are no plans to launch publicly at this time.</p>

<p><a href="press_sheet.pdf">Press Sheet</a> <a href="http://si.umich.edu/aboutsi/student-profile-b-time-spare">School of Information Spotlight</a></p>

<p>Selected parts of the EnRoute source code have been released on <a href="https://github.com/mattcolf/enroute-service">github</a>.</p>

